#include<iostream>

using namespace std;
int main()
{
    int a = 6, b= 3;
    cout<<"the sum of a and b is "<<(a+b)<<endl;
    cout<<"the difference of a and b is "<<(a-b)<<endl;
    cout<<"the product of a and b is "<<(a*b)<<endl;
    cout<<"the division of a and b is "<<(a/b)<<endl;
    cout<<"the modulo of a and b is "<<(a%b);
    return 0;
}